-- Приводим тестовые аккаунты к финальной схеме из 3 профилей:
-- 1) Обычный пользователь — регистрация по телефону (без изменений)
-- 2) Гость — просмотр тестовых документов, логин "guest", пароль назначит пользователь при первом входе
-- 3) Администратор — контроль подписок и тарифа "Для родных", логин "zaVed", пароль назначит пользователь при первом входе

-- Освобождаем логин zaVed: переименовываем гостевой аккаунт в guest, убираем персональное имя, сбрасываем пароль
UPDATE users
SET login = 'guest',
    full_name = 'Гость',
    password_hash = NULL
WHERE phone = '+70000000000';

-- Переименовываем существующий админ-аккаунт: логин admin -> zaVed, сбрасываем пароль для назначения нового
UPDATE users
SET login = 'zaVed',
    password_hash = NULL
WHERE phone = '+70000000001';